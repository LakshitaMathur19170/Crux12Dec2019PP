public class Pattern0 {

	public static void main(String[] args) {
		
		int n = 6 ;
		
		int row = 1 ;
		
		// rows : n
		while(row <= n) {
			
			// work
			System.out.print("*");
			
			// prep
			System.out.println();
			row = row + 1 ; 
		}

	}

}







